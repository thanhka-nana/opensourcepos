<p>Beste sportievelingen,</p>

<p>
<a href="http://www.ithemba.be/">Themba foundation</a> werkt al 5 jaar samen met de Baraa Primary school in het Arusha district in Tanzania.
</p>
<p>
Veel kinderen (zowel jongens als meisjes) van deze school hebben de natuurlijke aanleg om goed te kunnen hardlopen.
</p>
<p>
Onlangs is er een loopteam opgestart op de school zodat deze kinderen hun looptalenten kunnen gebruiken om wedstrijden mee te lopen.
De Baraa regio is een van de armste gebieden van het Arusha district waardoor de ouders van deze kinderen geen sportschoenen kunnen kopen.
</p> 
De iThemba foundation heeft via donaties al enkele voetbalbroekjes en truitjes kunnen aanbieden maar dat is natuurlijk niet genoeg.
Het Runwalk team heeft besloten om het ‘Baraa primary running team’ te helpen door 2de hands loopkledij en loopschoenen te verzamelen voor dit loopteam.
Daarom kan je je oude spullen dan komen inleveren tot en met <b>31 obktober</b> bij Runwalk in Herentals, wij zorgen voor het verdere transport.
</p>

<p>Wij danken jullie van harte voor jullie donaties.</p>

<p>Groetjes van Erik, Christa en het Baraa running team</p>

