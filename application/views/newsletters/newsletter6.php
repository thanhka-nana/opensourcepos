<p>
<span style="font-size:20px;font-weight:bold;color:#CC6600;font-family:arial;line-height:110%;">&#8220;Op weg naar betere sportprestaties dankzij de natuurlijke ondersteuning van CONCAP. &#8221;</span><br><br>
Beste sportieveling, <br><br>


Enkele jaren geleden begonnen wij, Erik en Christa, met onze systematische looptraining. We ontdekten samen het genoegen en de bewegingsvreugde in de loopsport.
Waar wij de eerste jaren ons bezig hielden met het opbouwen van basisuithouding (Erik had al 30 jaar ervaring in de loopsport terwijl mijn basis het minimale was), kregen we al vlug de smaak te pakken van competitie oa. wegwedstrijden, veldlopen en vanaf dit jaar ook pistemeeting. 
We sloten ons aan bij de <a href="http://www.acherentals.be">loopclub Achl</a> waar we nu ook beginnende en gevorderde lopers begeleiden.

Dat er nog heel wat moest getraind worden om in de eerste linie van de wedstrijd mee te lopen was al snel duidelijk. 
Na nog enkele jaren werken met trainingsschema's, zoeken naar een goede balans van trainen, werken, voeding, rusten en wat dan nog. kwam ik er toch al een heel eindje op 
vooruit. In een Belgisch kampioenschap haalde ik steeds net een tweede of een derde plaats. 
Erik die meer ambitie heeft om mij te begeleiden dan zelf nog prestaties neer te zetten, is momenteel erg gespecialiseerd in het opvolgen van voetanalyses en sportsteunzoolcorrecties bij blessures. Zonder zijn deskundige opvolging had ik er nooit aan kunnen denken te blijven lopen omwille van aanhoudende blessures. 
Onze runningspeciaalzaak is er dan ook om al uw fysiotechnische problemen op te lossen of om u gewoon de juiste (niet altijd de duurste) schoen aan te bieden. <br>
<br>
Tot een dag dat wij op aanraden van Luc en Dave een staaltjespakket CONCAP konden uitproberen. 
Erik is als eerste begonnen met het uittesten terwijl ik nog een afwachtende houding 
aannam. Psychologisch is het een doorduwer omdat je toch iets moet innemen maar achteraf bekeken 
valt het best mee, het zijn trouwens natuurlijke voedingssupplementen. Maar na een steeds 
enthousiastere Erik ben ik het dan ook beginnen te gebruiken. In het begin was het 
voor mij wat moeilijk omdat mijn darmen een erge winderigheid ontwikkelde, wat zeker niet 
aangenaam was. Mijn spijsvertering was daarvoor ook al niet zo optimaal en nu had ik de 
indruk dat alles overhoop lag. Blijkbaar was een grote opkuisbeurt van de darmflora hier op zijn plaats. 
Na een maand ongeveer voelde ik reeds een duidelijk verschil in de vermoeidheidsgraad bij de 
trainingen. Ik had meer eetlust, mijn metabolisme leek sneller te werken en ik kon de trainingen 
die ik anders met moeite kon afwerken beter aan. 
Een positief effect in een vicieuze cirkel kan je wel zeggen: beter trainen, betere prestaties en een 
hoger rendement in je levenskwaliteit! <br><br>
Ik zou nog even willen vermelden dat ik dit jaar al 2x Belgisch Kampioen was, 1x derde werd op een EK en 2x een Belgisch record verbeterd heb.
Dat ik nog niet aan het einde van mijn verhaal ben, is zeker!<br ><br >
Hierbij willen Erik en ik dan ook onze dank betuigen aan Luc en Dave dat wij <a href="http://www.concap.be">CONCAP</a> mogen
kennen. <br><br>
Het lijkt eenvoudig, maar wat we weten is dat CONCAP veelzijdige positieve effecten heeft, een 
produkt met onvermoede krachten, het proberen waard!
<p>Sportieve groetjes!<br>
Christa & Erik</p>

