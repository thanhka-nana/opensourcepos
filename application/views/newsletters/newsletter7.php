<p>Beste loopfanaten, </p>
<p>Na een geslaagde eerste editie <a href="http://picasaweb.google.com/jekkos/DeTienVanHertals#">vorig jaar</a>, hebben wij nu het genoegen u een 2de editie van <b>De tien van Hertals</b> te kunnen voorstellen. Deze zal plaatsvinden op <b>zondag 9 mei 2010</b> in het Bloso Sportcentrum te Herentals. 
Op die dag kunnen wij u een mooi en goed beloopbaar parcours over asfaltwegen en bospaden van Herentals aanbieden. Er wordt onderscheid gemaakt tussen de volgende categorie&euml;n:</p>
<ul>
<li>De jeugdreeksen starten vanaf 10u30
<ul>
<li>100m voor de jeugd tot 7 jaar oud</li>
<li>400m voor de jeugd van 7 tot 9 jaar oud</li>
<li>800m voor de jeugd van 9 tot 12 jaar oud</li>
</ul>
</li>
<li>6,5 km - Vrouwenloop recreatief - <b>Olivia Foundation Ladies Run</b> <a href="http://www.olivia.be">www.olivia.be</a> (kankerresearch kinderen) vanaf 11u00 </li>
<li>10,6 km wedstrijd vanaf 11u10</li>
</ul>

<p>Onze opbrengsten worden geschonken aan het hierboven vermelde kankerfonds!</p>
<p><u>Prijzentafel:</u></p>
<ul>
<li>Geldprijzen voor de eerste drie dames van de 6,5km</li>
<li>Geldprijzen voor de eerste drie van de 10,6km in de categorie&euml;n heren senioren, masters 40+, 50+ en 60+</li>
<li>De eerste drie jongens en meisjes van de jeugdloop winnen ook een extra prijs.</li>
<li>Er zullen mooie naturaprijzen onder de deelnemers verdeeld worden.</li>
</ul>

<p><u>Inschrijvingen:</u></p>
<ul>
<li>Voor de 6,5 en 10,6 km betaal je 7 euro mits voorinschrijven en 9 euro ter plekke aan de kassa. </li>
<li>Voor de jeugdreeksen is het 3 euro mits voorinschrijven en 4 euro ter plekke aan de kassa</li>
</ul>
Voorinschrijven kan je via <a href='<?=site_url('tienkm/subscribe')?>'>deze pagina</a>.<br><br></p>

