<p>
<span style="font-size:20px;font-weight:bold;color:#CC6600;font-family:arial;line-height:110%;">Opendeurdag op 14, 15 en 16 december!</span><br>
<span style="font-size:11px;font-weight:normal;color:#666666;font-style:italic;font-family:arial;">Tot 20% korting!</span><br>
In het weekend van 14, 15 en 16 december zetten wij onze deuren open! U kunt dan genieten van <strong>20%</strong> reductie op alle runningkledij. Vrijdag en zaterdag zijn we dan open tot 20u en zondag van 9u30 tot 13u. Een hapje en een drankje staan klaar voor u. 
</p>

<p>
<span style="font-size:20px;font-weight:bold;color:#CC6600;font-family:arial;line-height:110%;">Verbouwingen voltooid</span><br>
<span style="font-size:11px;font-weight:normal;color:#666666;font-style:italic;font-family:arial;">16 december 2007</span><br>
Onze runningshop steekt in een nieuw kleedje! Dit allemaal om u in de toekomst sneller te kunnen bedienen en een efficiente loopanalyse voor iedere klant te kunnen uitvoeren. In onze atelier kunnen we nu ook op een snelle manier schoencorrecties toepassen en eventueel steunzolen maken.</p>

<p>
<span style="font-size:20px;font-weight:bold;color:#CC6600;font-family:arial;line-height:110%;">Kerstshopping bij Runwalk</span><br>
<span style="font-size:11px;font-weight:normal;color:#666666;font-style:italic;font-family:arial;">Vanaf 14 december 2007</span><br>
Heb je nog een origineel, sportief kerstcadeau nodig? Bij ons vind je vele vrijetijdsartikelen waaronder winterjassen, sporttassen, badkledij, ea. in uitverkoop tijdens de eindejaarsperiode. Op al deze atrikelen geven wij tot <strong>50%</strong> korting. Kijk ook eens op onze website waar je onze laatste promoties kan vinden, waaronder de <a href="http://www.runwalk.be/index.php/site/details/1/0/159" target="_blank">Garmin</a>, een zeer nauwkeurige GPS en een must voor iedere loper.

</p>

<p>
<span style="font-size:20px;font-weight:bold;color:#CC6600;font-family:arial;line-height:110%;">Online oefeningen voor runners en nordic walkers</span><br>
<span style="font-size:11px;font-weight:normal;color:#666666;font-style:italic;font-family:arial;">Voel je fit en krachtig!</span><br> 
Voor mensen die houden van Nordic Walking, lopen of toch even op de onderhoudstoer willen gaan wat betreft het verstevigen van stabiliteitsspieren (romp, buik en rug) hebben wij nu een greep van het oefenpakket <a href="http://www.runwalk.be/index.php/site/details/2/0/160" target="_blank">online</a> gezet. Dit zijn oefeningen die u gemakkelijk kunt uitvoeren tijdens uw wandelingen of thuis. De oefeningen kunnen ook zonder poles uitgevoerd worden. Hebt u nog vragen ivm deze oefeningen, stuur ons gerust een <a href="malto:info@runwalk.be">mailtje</a>.

